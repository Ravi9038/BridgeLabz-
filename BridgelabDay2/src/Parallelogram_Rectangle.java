
public class Parallelogram_Rectangle {

	public static void main(String[] args) {
		
		for (int i = 0; i < 4; i++) {
			
			for (int j = 0; j < i; j++) {
				System.out.print(" ");
			}
			
			for (int k = 0; k < 5; k++) {
				
				System.out.print("*");
			}
			
			System.out.println();
		}
	}
}
